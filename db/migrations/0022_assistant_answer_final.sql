-- 0022_assistant_answer_final — 논사원 1차 답변문 사람 수정본(기억)
--
--   · answer_draft 는 논사원(AI)이 생성한 원본 초안으로 보존한다(불변).
--   · 상담원이 화면에서 답변문을 고쳐 저장하면, 그 최종본을 answer_final 에 기억한다.
--   · 저장 시 status 를 'edited' 로 바꾼다(목록/상세 표기 및 추후 학습 신호로 활용).
--   · 표시(목록/상세/복사)는 answer_final 이 있으면 그것을, 없으면 answer_draft 를 쓴다.
-- ════════════════════════════════════════════════════════════════════

alter table assistant_drafts
  add column if not exists answer_final text;  -- 상담원이 수정·확정한 최종 답변문(없으면 미수정)
